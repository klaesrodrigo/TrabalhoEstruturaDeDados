    public void chamaProximo() {

        if (fila.length() == 0) {
            proxCliente.setText("Fila Vazia");
        } else {
            proxCliente.setText("Cliente: " + fila.remove());
        }
    }

    public void atualizaEspera() {
        espera.setText("" + fila.length());
    }

    public void listaBarbeiros() {
        jTextArea2.setText(lista.retornVisualização());
    }

    public void limpaCampos() {
        jTextField4.setText("");
        jTextField5.setText("");
        txtNomeCliente.setText("");
        txtFoneCliente.setText("");
    }
