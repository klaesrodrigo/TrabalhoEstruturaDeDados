    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        if(lista.length() == 0){
            jTextArea2.setText("Não há barbeiros disponíveis");
        }
        
        if (lista.removeBy(new Barbeiro(jTextField4.getText(), jTextField5.getText())) == null) {
            jTextArea2.setText("Não há barbeiros com o nome " + jTextField4.getText() + " disponíveis");
            limpaCampos();
        } else {
            limpaCampos();
            jTextArea2.setText("OK! " + jTextField4.getText() + " esta a sua espera!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

